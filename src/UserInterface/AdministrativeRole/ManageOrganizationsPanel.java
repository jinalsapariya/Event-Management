    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        if(organizationJComboBox.getSelectedItem() == null){
           JOptionPane.showMessageDialog(null, "Please make a selection for type of organization.", "Info", JOptionPane.ERROR_MESSAGE);
            return;   
        }
        Type type = (Type) organizationJComboBox.getSelectedItem();
        ArrayList<Organization> o = enterprise.getOrganizationDirectory().getOrganizationList();
        boolean validate = enterprise.getOrganizationDirectory().checkIfOrgTypeExists(type, o);
        if(validate == true){
            JOptionPane.showMessageDialog(null, "The Organization you are trying to create already exists.");
            return;
        }
        directory.createOrganization(type);
        populateTable();
    }//GEN-LAST:event_addJButtonActionPerformed

