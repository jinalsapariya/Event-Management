    private void populateCombo(){
        organizationJComboBox.removeAllItems();
        if (enterprise instanceof CompanyEnterprise) {
            organizationJComboBox.addItem(Organization.Type.HRDept);
            organizationJComboBox.addItem(Organization.Type.FinanceDept);
        } else if (enterprise instanceof UniversityEnterprise){
            organizationJComboBox.addItem(Organization.Type.EventOrganizingCommittee);
            organizationJComboBox.addItem(Organization.Type.DirectorOffice);
        } else if (enterprise instanceof EventPlannerEnterprise) {
            organizationJComboBox.addItem(Organization.Type.BudgetingDept);
        }
    }

    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : directory.getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrgId();
            row[1] = organization.getName();
            
            model.addRow(row);
        }
    }
