    private void jPasswordField2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPasswordField2KeyTyped
        // TODO add your handling code here:
        String c = "" + evt.getKeyChar();
        String checker = jPasswordField2.getText();
        if (!c.isEmpty()) {
            checker += c;
        }
        if (jPasswordField1.getText().equals(checker)) {
            jPasswordField2.setBackground(Color.green);
            jButton1.setEnabled(true);
        } else {
            jPasswordField2.setBackground(Color.red);
            jButton1.setEnabled(false);
        }
    }//GEN-LAST:event_jPasswordField2KeyTyped

