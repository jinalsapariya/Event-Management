    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        // TODO add your handling code here:
        if (ValidationMethods.isStringValidWithSpace(fnametxt.getText()) == false) {
            JOptionPane.showMessageDialog(null, "Please enter valid input", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (ValidationMethods.isStringValidWithSpace(lnametxt.getText()) == false) {
            JOptionPane.showMessageDialog(null, "Please enter valid input", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (ValidationMethods.isEmailValid(emailtxt.getText()) == false) {
            JOptionPane.showMessageDialog(null, "Please enter valid Email-id", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (ValidationMethods.isStringValidContact(contacttxt.getText()) == false) {
            JOptionPane.showMessageDialog(null, "Please enter valid Phone Number", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (ValidationMethods.isStringValidWithSpace(usernametxt.getText()) == false) {
            JOptionPane.showMessageDialog(null, "Please enter username", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String passwordreal = jPasswordField1.getText();
        String passwordconfirm = jPasswordField2.getText();
        if (ValidationMethods.isPasswordAndConfirmPasswordSame(passwordreal, passwordconfirm)==false) {
            JOptionPane.showMessageDialog(null, "", "Info", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String username = usernametxt.getText();
        for(Customer cust: system.getCustomerList().getCustomerList()){
            if(system.checkIfUserIsUnique(username)){
                if(system.checkIfUserIsUnique(username)==false){
                    JOptionPane.showMessageDialog(null, "Username is already taken by a customer");
                    return;
                }
            }
        }
        for(Network net: system.getNetworkList()){
            if(system.checkIfUserIsUnique(username)==false){
                JOptionPane.showMessageDialog(null, "Username is already assigned.");
                return;
            }
            for(Enterprise ent: net.getEnterpriseDirectory().getEnterpriseList()){
                if(ent.getUserDirectory().checkIfUsernameIsUnique(username)==false){
                    JOptionPane.showMessageDialog(null, "Username is already assigned.");
                    return;
                }
                for(Organization org: ent.getOrganizationDirectory().getOrganizationList()){
                    if(org.getUserDirectory().checkIfUsernameIsUnique(username)==false){
                        JOptionPane.showMessageDialog(null, "Username is already assigned.");
                        return;
                    }
                }
            }
        }
        
        
        String fname = fnametxt.getText();
        String lname = lnametxt.getText();
        String email = emailtxt.getText();
        String contact = contacttxt.getText();
        String message = "Congratulations! You have been successfully registered."+fname+" "+lname+"!";
        system.getCustomerList().createCustomer(fname, lname, email, contact);
        system.getUserDirectory().createCustomerAccount(username, passwordreal, role);
        JOptionPane.showMessageDialog(null, "You have been successfully registered. You can login now.");
        
        
        
        dB4OUtil.storeSystem(system);
        try {
            EmailAuth.postMail(email, message, sender);
        } catch (MessagingException ex) {
            Logger.getLogger(RegistrationPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

